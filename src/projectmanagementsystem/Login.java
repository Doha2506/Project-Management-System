    // check if the employee is exist in the company
    public static boolean checkExistenceOfEmployee(int id,String name) throws IOException { 
        Scanner input = new Scanner(file);
        while (input.hasNextLine()) {
            if(id ==  input.nextInt()  ){ // read the id
                input.next(); // ignore the position
                String n = input.next();
                if(name.equalsIgnoreCase(n))
                   return true;  // means we have an employee of this id
                else{
                    JOptionPane.showMessageDialog(null,"The name is wrong", "ERROR",JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            }
            input.nextLine();   // ignore the rest of the line
        }
        input.close();
        JOptionPane.showMessageDialog(null,"We don't have employee with this ID", "ERROR",JOptionPane.ERROR_MESSAGE);
        return false;  // means that we don't have employee of this id
    }
    
    static String checkPosition(int id) throws IOException{
        Scanner input = new Scanner(file);
        while (input.hasNextLine()) {
            if(id ==  input.nextInt()){
                return input.next();
            }
            input.nextLine();
        }
        return null;
    }
    
    

