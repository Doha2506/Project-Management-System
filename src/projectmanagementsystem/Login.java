    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        int ID = Integer.parseInt(id.getText());
        String n = name.getText();
        try {
            if(checkExistenceOfEmployee(ID,n) == true){
                JOptionPane.showMessageDialog(null,"Login Successfully", "Success",JOptionPane.INFORMATION_MESSAGE);
                dispose();
                if(checkPosition(ID).equalsIgnoreCase("Employee")){
                    EmployeeOptions o = new EmployeeOptions();
                    o.setVisible(true);
                }
                else if(checkPosition(ID).equalsIgnoreCase("Admin")){
                    adminOptions o = new adminOptions();
                    o.setVisible(true);
                }
                else if(checkPosition(ID).equalsIgnoreCase("TeamLeader")){
                    TeamLeaderOptions o = new TeamLeaderOptions();
                    o.setVisible(true);
                }
                else if(checkPosition(ID).equalsIgnoreCase("PMLeader")){
                    PmOptions o = new PmOptions();
                    o.setVisible(true);
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginActionPerformed

